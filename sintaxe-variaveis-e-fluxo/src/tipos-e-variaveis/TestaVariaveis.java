public class TestaVariaveis {
    public static void main(String[] args) {
        System.out.println("Hello, World!");
        
        int idade; /* Declaração da variavel */
        
        idade = 37; /* Atribuindo valor há variavel */

        System.out.println("idade: " + idade);

        idade = 30 + 10;

        System.out.println("Soma: " + idade);

        idade = 7 * (5 + 2); /* Mudando a predecência aritimética */

        System.out.println("Soma e multiplicação: " + idade);
    }
}
